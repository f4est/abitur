const loadStats = async () => {
  isLoadingStats.value = true
  try {
    // –ó–∞–º–µ–Ω—è–µ–º useFetch –Ω–∞ –ø—Ä—è–º–æ–π –≤—ã–∑–æ–≤ fetch –∏ –æ–±—Ä–∞–±–æ—Ç–∫—É —á–µ—Ä–µ–∑ await response.json()
